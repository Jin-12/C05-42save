int		ft_iterative_factorial(int nb)
{
	int i;

	i = nb;
	if (i == 0 || i == 1)
		return (1);
	if (i < 0)
		return (0);
	while (i != 1)
	{
		i--;
		nb = nb * i;
	}
	return (nb);
}
